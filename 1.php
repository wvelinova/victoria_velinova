//Извеждаме на екрана числата от 1 до 10, разделени с "-"
//Използваме for:
for ($i=1;$i<=10;$i++){
 //Проверяваме дали числото е равно на 10, защото след 10 не трябва да има '-'   
    if ($i === 10) {
 // отпечатваме числото      
        echo $i;
 //Ако не е 10 го отпечатваме с '-' след числото       
    } else {
        echo $i . "-";
    }
            
}

